
public class tablaMultiplicacion {

	public static void main(String[] args) {  //main +crtl+space
		
		
		for (int contador=0; contador<=10;contador++) {
			
			for (int multi=0; multi<=10; multi++) {
				System.out.print(contador*multi);
				System.out.print(" ");
			}
			System.out.println();
		}
	}

}
